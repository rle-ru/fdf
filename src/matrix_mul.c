#include "fdf.h"
#include "libft.h"

void	mat_4_mul(t_matrix *a, t_matrix b)
{
	int	x;
	int	y;
	t_matrix	m;

	y = -1;
	while (++y < 4&& (x = -1))
		while (++x < 4)
			m[y][x] = (*a)[y][0] * b[0][x]
				+ (*a)[y][1] * b[1][x]
				+ (*a)[y][2] * b[2][x]
				+ (*a)[y][3] * b[3][x];
	ft_memcpy(a, &m, sizeof(t_matrix));
}
